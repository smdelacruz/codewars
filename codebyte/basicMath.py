import math

print(math.pi)  #3.14....
print(math.floor(5.9))  #5.0
print(int(math.floor(5.9)))  #5
print(int(math.ceil(5.01)))  #6
print(math.exp(5))  #148.41.... (exponential ...)
print(math.pow(5, 2))  #25.0 (5 to the 2nd power)
print(5**2)  #25 (5 to the 2nd power; similar to math.pow())
print(math.factorial(5))  #120 (factorial...)
print(math.fabs(-5))  #5 (the same as function abs(-5))
